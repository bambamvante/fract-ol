#include "ft_printf.h"

int	ft_putunbr(unsigned int nbr)
{
	unsigned long	n;
	int				i;

	n = nbr;
	i = 0;
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	else
		i += ft_putchar(n + 48);
	return (i);
}
