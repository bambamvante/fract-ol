#include "ft_printf.h"

int	ft_putnbr(int nbr)
{
	long	n;
	int		i;

	n = nbr;
	i = 0;
	if (n < 0)
	{
		i += write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	else
		i += ft_putchar(n + 48);
	return (i);
}
